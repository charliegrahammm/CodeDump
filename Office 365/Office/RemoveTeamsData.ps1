.SYNOPSIS  
    This script removes all Microsoft Teams data from a machine.
.DESCRIPTION  
    Removes all Microsoft Teams data from a machine. Must be ran as the logged in user. It is recommended to reboot before and after this script has ran.
.NOTES  
    File Name  : RemoveTeamsData.ps1  
    Author     : Charlie Graham 
    Requires   : PowerShell v2
#>

# Kill Teams
TASKKILL /F /IM TEAMS.EXE

# Get users
Write-Output "Gathering Users"
$users = Get-ChildItem -Path "C:\Users"

# Loop through users and delete the file
Write-Output "Deleting Teams Data"
$users | ForEach-Object {
    Remove-Item -Path "C:\Users\$($_.Name)\AppData\Local\Microsoft\Teams" -Force -Recurse -ErrorAction Ignore
}
