.SYNOPSIS  
    This script sets Windows to automatically sign in on boot with the credentials specified.
.DESCRIPTION  
    Enter the credentials in the below parameters and run on a target machine to set auto-login. 
.NOTES  
    File Name  : AutoLogin.ps1  
    Author     : Charlie Graham 
    Requires   : PowerShell v2
#>

# Enter credentials below
$DefaultUsername = "your username"
$DefaultPassword = "your password"

# Registry key path
$RegPath = "HKLM:\SOFTWARE\Microsoft\Windows NT\CurrentVersion\Winlogon"

# Set key
Set-ItemProperty $RegPath "AutoAdminLogon" -Value "1" -type String 
Set-ItemProperty $RegPath "DefaultUsername" -Value "$DefaultUsername" -type String 
Set-ItemProperty $RegPath "DefaultPassword" -Value "$DefaultPassword" -type String

