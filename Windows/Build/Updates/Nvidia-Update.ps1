.SYNOPSIS  
    This script Installs Chocolatey then installs NVidia Display Driver and forces a check for an update.
.DESCRIPTION  
    Uses Chocolatey NVidia Display Driver to update Nvidia Drivers.
.NOTES  
    File Name  : Nvidia-Update.ps1  
